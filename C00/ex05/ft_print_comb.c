#include <unistd.h>

void	ft_print_comb(void)
{
	char	c;
	char	d;
	char	u;

	c = '0' - 1;
	while (++c <= '9')
	{
		d = c;
		while (++d <= '9')
		{
			u = d + 1;
			while (u <= '9')
			{
				write(1, &c, 1);
				write(1, &d, 1);
				write(1, &u, 1);
				if (c < '7')
				{
					write(1, ",", 1);
					write(1, " ", 1);
				}
				u++;
			}
		}
	}
}
